function getCuratedSummary(scenarioId: DemoScenarioId): CuratedAnswerSummary | undefined {
  return curatedAnswerSummaries[String(scenarioId)];
}

export function applyCuratedScenarioSummary(result: SearchResult): SearchResult {
  const summary = getCuratedSummary(result.scenarioId);

  if (!summary) {
    return result;
  }

  return {
    ...result,
    answer: {
      ...result.answer,
      shortConclusion: summary.shortConclusion,
      confidenceReason: summary.confidenceReason,
      warnings: summary.warnings,
    },
  };
}
